import os
import requests
from zipfile import ZipFile

# Directory to save the downloaded images
download_dir = "downloaded_images"
os.makedirs(download_dir, exist_ok=True)

# Download each image and save it locally
for idx, url in enumerate(image_urls):
    response = requests.get(url)
    if response.status_code == 200:
        with open(os.path.join(download_dir, f"image{idx+1}.jpg"), "wb") as f:
            f.write(response.content)
            
    print(f"Downloaded image {idx+1}/{len(image_urls)}", end="\r")

# Create a ZIP file and add all the downloaded images to it
zip_filename = "images.zip"
with ZipFile(zip_filename, "w") as zipf:
    for root, _, files in os.walk(download_dir):
        for file in files:
            zipf.write(os.path.join(root, file), arcname=file)

